class PostsController < ApplicationController
  protect_from_forgery with: :exception
  prepend_before_action :authenticate_user!, except: [:index, :show]
  before_action :set_post, only: [:show, :edit, :update, :destroy]

  # GET /posts
  # GET /posts.json
  def index
    @posts = Post.all 
    # paginationNumber = 3
    # if user_signed_in?
